/*
    TOPIC: Stack - Introduction

    Stack is data structure which maintains information in the LIFO order.
    LIFO -> Last In First Out

    Stack can be inplemented using Array,
                             using Linked List,
                             using Vector

    Important operation in stack:
    - Push (Insertion of data)
    - Pop  (Removal of data)
    - Top  (viewing the topmost element)

    - isEmpty() (check if stack is empty)
    - isFull()  (check if stack is full)
*/